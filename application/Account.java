    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
    String schoolId = schoolIdField.getText().trim();

    if (schoolId.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill out the school ID.", "Incomplete Data", JOptionPane.WARNING_MESSAGE);
        return; // Do not proceed if the school ID field is empty
    }

    AccountEdit accountEdit = new AccountEdit();

    // Call the fetchSchoolIdFromDatabase method
    String schoolIdData = accountEdit.fetchSchoolIdFromDatabase(schoolId);

    if (schoolIdData != null) {
        // Display the data using the fetched schoolIdData
        displayData(schoolIdData);
    } else {
        JOptionPane.showMessageDialog(null, "No school ID found in the database!", "School ID Not Found", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_searchButtonActionPerformed
    
    void displayData(String schoolId) {
        try {
            Class.forName("org.postgresql.Driver");
            try (Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/UserInfo", "studyflow", "1234");
                 PreparedStatement pstmt = conn.prepareStatement("SELECT username, name, bday, gender FROM login.accounts WHERE school_id = ?")) {

                pstmt.setString(1, schoolId);
                ResultSet rs = pstmt.executeQuery();

                if (rs.next()) {
                    String username = rs.getString("username").trim();
                    String name = rs.getString("name").trim();
                    Date bday = rs.getDate("bday");
                    String genderL = rs.getString("gender").trim();

                    // Display the data
                    usernameLabel.setText(username);
                    nameLabel.setText(name);
                    bdayLabel.setText(bday != null ? bday.toString() : ""); // Convert to String
                    genderLabel.setText(genderL);

                    // Optionally, you can display the school ID too
                    schoolID.setText("School ID: " + schoolId);
                } else {
                    JOptionPane.showMessageDialog(null, "No record found for the provided school ID.", "Record Not Found", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    
    
