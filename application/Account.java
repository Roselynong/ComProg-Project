    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
    String schoolId = schoolIdField.getText().trim();

    if (schoolId.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill out the school ID.", "Incomplete Data", JOptionPane.WARNING_MESSAGE);
        return; // Do not proceed if the school ID field is empty
    }

    int confirmation = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record in the table?", "Confirmation", JOptionPane.YES_NO_OPTION);

    if (confirmation == JOptionPane.YES_OPTION) {
        try {
            Class.forName("org.postgresql.Driver");
            try (Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5432/UserInfo", "studyflow", "1234");
                 PreparedStatement pstmt = conn.prepareStatement("DELETE FROM login.accounts WHERE school_id = ?")) {

                pstmt.setString(1, schoolId); // Set the school ID parameter

                int rowsAffected = pstmt.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Record deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "No record found to delete.", "No Records", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_deleteButtonActionPerformed

